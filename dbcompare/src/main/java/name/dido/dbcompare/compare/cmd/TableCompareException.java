public class TableCompareException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8469050289493387560L;

	public TableCompareException() {
		super();
	}

	public TableCompareException(String message, Throwable cause) {
		super(message, cause);
	}

	public TableCompareException(String message) {
		super(message);
	}

	public TableCompareException(Throwable cause) {
		super(cause);
	}

}
